# 3-1. 為什麼 1-1 和 1-2 差這麼多，底層是什麼原因？
#
#   【Python List - 慢的原因】
#   Python List 在記憶體裡「不是連續存放」的。
#   它存的其實是一堆「指標」，每個指標再指向散落在各處的 Python 物件。
#
#   記憶體示意圖：
#   List:  [ptr1] [ptr2] [ptr3] ...
#              ↓      ↓      ↓
#           [1.5]  [1.5]  [1.5]   ← 散落在記憶體各個角落
#
#   所以 for loop 跑 500 萬次，每次都要：
#     ① 跟著指標跳到那個物件的記憶體位置（指標追蹤）
#     ② 把 Python 物件解包，取出真正的數字
#     ③ 做加法
#   這三步驟重複 500 萬次，overhead 非常大。
#
#   【NumPy Array - 快的原因】
#   NumPy Array 把所有數字「緊密排在一起」存在連續的記憶體區塊。
#
#   記憶體示意圖：
#   Array: [1.5][1.5][1.5][1.5][1.5]...  ← 像火車車廂，一節接一節
#
#   好處有兩個：
#     ① CPU 的「快取（Cache）」可以一次載入一大批資料，不用一個個去找
#     ② .sum() 是用 C 語言寫的底層函式，直接對記憶體操作，
#        完全不經過 Python 的物件系統，速度快上 10～50 倍


# 3-2. 為什麼 2-1 和 2-2 差這麼多，底層是什麼原因？
#
#   【Python List .append() - 快的原因】
#   Python List 背後是「動態陣列」設計，它有預留空位的機制：
#
#   一開始就偷偷多申請空間：
#   [■][■][■][  ][  ][  ][  ][  ]
#    已用資料    預留的空位
#
#   append 時直接填入空位，不需要搬家：
#   [■][■][■][■][  ][  ][  ][  ]  ← O(1)，超快
#
#   空位用完才擴容，而且每次「容量 x2」：
#   [■][■][■][■][■][■][■][■][  ][  ][  ]...[  ]
#   搬家雖然偶爾發生，但因為容量每次加倍，
#   搬家次數只有 log₂(n) 次，平均每次 append 成本趨近 O(1)
#
#   【NumPy np.append() - 慢的原因】
#   NumPy 的連續記憶體在「新增資料」這件事上變成了缺點。
#   因為緊密排列，後面完全沒有預留空位：
#
#   [1.5][1.5][1.5]...[1.5]█████  ← █ 代表被其他資料佔用的記憶體
#
#   想加新資料，但後面已經沒位置了！
#   唯一辦法：在記憶體其他地方重新申請一塊「600萬格」的空間，
#   把舊的 500 萬筆 + 新的 100 萬筆，全部一起複製過去。
#
#   舊陣列：[1.5]...[1.5]           ← 整個廢棄丟掉 🗑️
#   新陣列：[1.5]...[1.5][2.0]...[2.0]  ← 全新複製的
#
#   這是完整的 O(n) 操作，n 越大越痛。


# 3-3. 描述 2-2 和上課的什麼觀念有關（詳答）
#
#   2-2 的行為，完整對應到上課說的「Array（靜態陣列）」的核心特性。
#
#   【Array 的本質】
#   Array 在宣告時，就向作業系統申請「固定大小的連續記憶體」。
#   這塊空間的大小在一開始就決定好，之後不能變動。
#
#   為什麼不能直接在後面延伸？
#   因為作業系統把記憶體分配給很多程式，
#   Array 後面的記憶體可能已經被別人用了：
#
#   記憶體：...[你的Array: 500萬格][別人的資料][別人的資料]...
#
#   你不能直接往後擴，那塊不是你的地盤！
#
#   【所以想「擴充」只有一條路：搬家】
#     步驟 1：在記憶體其他地方，申請一塊「更大的新空間」（500萬 + 100萬）
#     步驟 2：把原本 500 萬筆資料，一筆一筆複製到新空間
#     步驟 3：把新的 100 萬筆資料放在後面
#     步驟 4：釋放掉舊的記憶體空間
#
#   這個過程的時間複雜度是 O(n)，n 是原本陣列的大小。
#   資料越多，搬家越慢。
#
#   【NumPy 就是 Array 的實作】
#   NumPy Array 完全遵守這個特性：
#     - 連續記憶體 ✅
#     - 固定大小，不能動態擴充 ✅
#     - 想新增資料，只能建立全新陣列再複製 ✅
#
#   np.append() 之所以慢，就是因為它底層做的事，
#   正是教科書上 Array 「插入/擴充」操作的標準流程：
#   「沒有空位 → 申請新空間 → 整批搬家 → 釋放舊空間」
#
#   總結
#   2-2 示範的就是「Array 固定大小、無法原地擴充」的根本限制，
#   也是為什麼後來需要發明「Linked List」和「Dynamic Array」
#   來解決這個問題的原因。